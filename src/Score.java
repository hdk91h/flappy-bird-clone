public class Score {
    
    private double playerScore = 0;
    private double playerHighScore = 0;
    private double playerLastScore = 0;

    public double getPlayerScore() {
        return playerScore;
    }
    public void setPlayerScore(double playerScore) {
        this.playerScore = playerScore;
    }

    public double getPlayerHighScore() {
        return playerHighScore;
    }
    public void setPlayerHighScore(double playerHighScore) {
        this.playerHighScore = playerHighScore;
    }
    public double getPlayerLastScore() {
        return playerLastScore;
    }
    public void setPlayerLastScore(double playerLastScore) {
        this.playerLastScore = playerLastScore;
    }
}
